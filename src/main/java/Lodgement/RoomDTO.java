package Lodgement;

public class RoomDTO {
	String roomName, roomType, roomCount, capacity, roomIntro, convenienceFacility, lentPrice, onenightPrice;

	public String getRoomName() {
		return roomName;
	}

	public void setRoomName(String roomName) {
		this.roomName = roomName;
	}

	public String getRoomType() {
		return roomType;
	}

	public void setRoomType(String roomType) {
		this.roomType = roomType;
	}

	public String getRoomCount() {
		return roomCount;
	}

	public void setRoomCount(String roomCount) {
		this.roomCount = roomCount;
	}

	public String getCapacity() {
		return capacity;
	}

	public void setCapacity(String capacity) {
		this.capacity = capacity;
	}

	public String getRoomIntro() {
		return roomIntro;
	}

	public void setRoomIntro(String roomIntro) {
		this.roomIntro = roomIntro;
	}

	public String getConvenienceFacility() {
		return convenienceFacility;
	}

	public void setConvenienceFacility(String convenienceFacility) {
		this.convenienceFacility = convenienceFacility;
	}

	public String getLentPrice() {
		return lentPrice;
	}

	public void setLentPrice(String lentPrice) {
		this.lentPrice = lentPrice;
	}

	public String getOnenightPrice() {
		return onenightPrice;
	}

	public void setOnenightPrice(String onenightPrice) {
		this.onenightPrice = onenightPrice;
	}
}
